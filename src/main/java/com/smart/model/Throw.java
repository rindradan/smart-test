package com.smart.model;

public class Throw
{
    private int index;
    private int frame;
    private int nbKeel;

    public int getIndex()
    {
        return index;
    }

    public void setIndex(int index)
    {
        this.index = index;
    }

    public int getFrame()
    {
        return frame;
    }

    public void setFrame(int frame)
    {
        this.frame = frame;
    }

    public int getNbKeel()
    {
        return nbKeel;
    }

    public void setNbKeel(int nbKeel)
    {
        this.nbKeel = nbKeel;
    }
}
